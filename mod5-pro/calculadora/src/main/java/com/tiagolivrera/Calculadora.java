package com.tiagolivrera;

/*
 * Classe que implementa os métodos básicos de uma calculadora (somar, subtrair, multiplicar e dividir)
 */
public class Calculadora {
    
    // método que retorna a soma entre dois números
    public static int adicionar(int a, int b) {
        return a + b;
    }

    // método que retorna a subtração entre dois números
    public static int subtrair(int a, int b) {
        return a - b;
    }

    // método que retorna a multiplicação entre dois números
    public static int multiplicar(int a, int b) {
        return a * b;
    }

    // método que retorna a divisão entre dois números
    public static int dividir(int a, int b) {
        return a / b;
    }
}
